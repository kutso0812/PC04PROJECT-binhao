<h1>LATAR BELAKANG ONEXOX</h1>
<p>One XOX Sdn Bhd merupakan sebuah syarikat Bumiputera dan anak syarikat kepada XOX Berhad yang telah beroperasi sebagai Pengendali Rangkaian Mudah Alih Maya (MVNO) sejak tahun 2005. One XOX Sdn Bhd yang telah ditubuhkan pada 2014, secara eksklusifnya membawa jenama ONEXOX bagi memasarkan produk XOX dengan pelan perkhidmatan mudah alih yang unik dan tersendiri.
    One XOX percaya akan model perniagaan yang cekap, telus dan sedaya upaya mematuhi tuntutan syariah untuk mempastikan kadar perkhidmatan adalah sangat berpatutan, tiada iklan-iklan yang mengelirukan serta tiada unsur-unsur caj tersembunyi dapat menarik minat dikalangan pelanggan-pelanggan perkhidmatan mudah alih yang sentiasa mencari alternatif penjimatan.
    Disamping penawaran perkhidmatan yang sangat kompetitif, One XOX yang tidak mengamalkan pengiklanan secara terbuka telah membuka peluang perniagaan telekomunikasi 'sebenar' mampu milik kepada semua individu yang berminat dengan menawarkan insentif lumayan dan berpanjangan berkonsepkan perkongsian keuntungan syarikat.
    One XOX mengalu-alukan semua pelanggan yang dahagakan penjimatan serta usahawan-usahawan yang sedang giat mencari peluang perniagaan untuk mencuba perkhidmatan telus tanpa caj tersembunyi dan memanfaatkan platform perniagaan yang ditawarkan.<br>

